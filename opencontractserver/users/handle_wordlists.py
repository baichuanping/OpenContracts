"""
Curated word lists for Reddit-style auto-assigned user handles.

The lists are sized so that the cartesian product (``ADJECTIVES × NOUNS``)
yields a comfortable handle namespace where collisions are rare. Both lists are
hand-curated to avoid offensive terms, brand names, and ambiguous spellings; do
**not** swap to a third-party word list without re-vetting.

Adding new entries grows the namespace and is safe — the management command
``regenerate_user_handles`` can be re-run after a list change to re-roll
handles for users who previously got a numeric-suffixed candidate due to a
collision.

Format requirements:
- ASCII only
- Lower-case
- No spaces, hyphens, or punctuation (we camelCase the pair)
- Length 3-12 characters
- Each word must be unique to its list (no entry appears in both
  ``ADJECTIVES`` and ``NOUNS``) so combinations like ``cometComet`` are
  impossible. The pair lists are spot-checked for cross-overlap; add
  new entries to whichever list reads more naturally.
"""

